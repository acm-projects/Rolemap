    job = (row.get("job_name") or row.get("job") or "").strip()
    desc = (row.get("description") or "").strip()
    qualifications_raw = row.get("qualifications") or row.get("skills") or ""

    # Parse the qualifications column (usually the matched job title from scraper)
    base_skills_raw = parse_skills(qualifications_raw)

    # Normalize base skills but immediately flag & strip job title strings
    base_skills_clean = []
    for s in base_skills_raw:
        norm = normalize_skill(s).strip()
        if not norm:
            continue
        # Keep only non-job-title entries from qualifications
        if not is_job_title(norm):
            base_skills_clean.append(norm)
    # base_skills_raw → store original_skills as-is for audit
    original_skills_str = "|".join(normalize_skill(s).upper() for s in base_skills_raw if s)

    # Extract from job title + description using full vocabulary
    dynamic = extract_skills_from_text(f"{job}\n{desc}", vocab)

    # Merge: base (non-title) + dynamic
    merged_lower = set()
    for s in base_skills_clean:
        merged_lower.add(s.lower())
    for s in dynamic:
        merged_lower.add(s.lower())

    # Build self-referential token set — skills matching the hiring company's
    # own name are blocked for that row (Notion shouldn't list Notion as a skill)
    self_tokens = company_self_tokens(company)

    # Validate and split into graph-matched vs unmatched
    validated = []
    unmatched = []

    for skill_lower in sorted(merged_lower):
        if not is_valid_skill_label(skill_lower):
            continue
        # Drop skills that are just the company's own name/product
        if skill_lower in self_tokens:
            continue
        graph_match = validate_against_graph(skill_lower, concept_lookup, all_concepts_lower)
        if graph_match:
            validated.append(graph_match)
        else:
            # Keep unmatched skills that pass quality checks — they may be
            # real emerging skills not yet in the graph
            display = skill_lower.upper()
            if is_valid_skill_label(display):
                unmatched.append(display)

    # Deduplicate and cap
    validated = sorted(set(validated))[:MAX_SKILLS_PER_ROW]
    unmatched = sorted(set(unmatched))[:MAX_SKILLS_PER_ROW]

    # Canonical output form: uppercase for display consistency
    validated_upper = [v.upper() for v in validated]

    out = dict(row)
    out.pop("description", None)
    out["company"] = company
    out["job"] = job
    out["original_skills"] = original_skills_str
    out["normalized_skills"] = "|".join(validated_upper)
    out["unmatched_skills"] = "|".join(unmatched)
    out["skill_count"] = str(len(validated_upper))
    out["is_noise_row"] = "1" if is_noise_row(company, job, desc) else "0"
    return out


