# STATS REPORTER
# =============================================================================
def print_stats(rows: list[dict]) -> None:
    counts = [int(r.get("skill_count", 0)) for r in rows]
    non_noise = [r for r in rows if r.get("is_noise_row") != "1"]
    counts_clean = [int(r.get("skill_count", 0)) for r in non_noise]

    total = len(rows)
    noise = sum(1 for r in rows if r.get("is_noise_row") == "1")
    zero_skills = sum(1 for c in counts_clean if c == 0)
    one_skill = sum(1 for c in counts_clean if c == 1)

    if counts_clean:
        avg = sum(counts_clean) / len(counts_clean)
        mx = max(counts_clean)
        mn = min(counts_clean)
    else:
        avg = mx = mn = 0

    print(f"\n{'-'*50}")
    print(f"Total rows        : {total}")
    print(f"Noise rows        : {noise}")
    print(f"Clean rows        : {len(non_noise)}")
    print(f"Rows with 0 skills: {zero_skills}")
    print(f"Rows with 1 skill : {one_skill}  (should be near 0 after fix)")
    print(f"Avg skills/row    : {avg:.1f}")
    print(f"Min / Max skills  : {mn} / {mx}")

    # Top-25 skills across all rows
    from collections import Counter
    skill_counter: Counter = Counter()
    for r in non_noise:
        for skill in r.get("normalized_skills", "").split("|"):
            skill = skill.strip()
            if skill:
                skill_counter[skill] += 1

    print(f"\nTop 25 skills across all rows:")
    for skill, cnt in skill_counter.most_common(25):
        bar = "#" * min(cnt, 40)
        print(f"  {skill:<40s} {cnt:3d}  {bar}")

    # Unmatched skills summary
    unmatched_counter: Counter = Counter()
    for r in non_noise:
        for skill in r.get("unmatched_skills", "").split("|"):
            skill = skill.strip()
            if skill:
                unmatched_counter[skill] += 1

    if unmatched_counter:
        print(f"\nTop 20 unmatched skills (not in graph -- consider adding):")
        for skill, cnt in unmatched_counter.most_common(20):
            print(f"  {skill:<40s} {cnt:3d}")

    print(f"{'-'*50}\n")


