def main():
    parser = argparse.ArgumentParser(description="Normalize LinkedIn scrape CSV for tuning")
    parser.add_argument("--in", dest="in_path", default="in.csv")
    parser.add_argument("--out", dest="out_path", default="out.csv")
    parser.add_argument("--drop-noise", action="store_true", help="Drop rows flagged as noise")
    parser.add_argument("--stats-only", action="store_true", help="Print stats from existing out.csv without re-processing")
    args = parser.parse_args()

    script_dir = Path(__file__).parent
    in_path = Path(args.in_path)
    out_path = Path(args.out_path)

    if args.stats_only:
        if not out_path.exists():
            raise FileNotFoundError(f"No output file found at {out_path}")
        with open(out_path, newline="", encoding="utf-8") as f:
            rows = list(csv.DictReader(f))
        print_stats(rows)
        return

    if not in_path.exists():
        raise FileNotFoundError(f"Input CSV not found: {in_path}")

    # Load concepts vocabulary
    extractable_concepts, all_concepts = load_concepts(script_dir)

    # Build extraction vocabulary
    vocab = build_vocab(extractable_concepts)
    print(f"Extraction vocabulary size: {len(vocab)} terms")

    # Build concept lookup for validation
    concept_lookup = build_concept_lookup(all_concepts)
    all_concepts_lower = list(concept_lookup.keys())
    print(f"Graph concept lookup size : {len(concept_lookup)} concepts")
    if not _FUZZY_AVAILABLE:
        print("WARNING: thefuzz not installed — fuzzy matching disabled (exact only)")

    # Load input
    with open(in_path, newline="", encoding="utf-8") as f:
        rows = list(csv.DictReader(f))
    print(f"Input rows: {len(rows)}")

    # Normalize
    normalized_rows = [
        normalize_row(row, vocab, concept_lookup, all_concepts_lower)
        for row in rows
    ]

    if args.drop_noise:
        normalized_rows = [r for r in normalized_rows if r.get("is_noise_row") != "1"]

    # Determine output columns
    all_columns: set[str] = set()
    for row in normalized_rows:
        all_columns.update(row.keys())
    all_columns.discard("description")

    preferred = [
        "company", "job", "job_type",
        "original_skills", "normalized_skills", "unmatched_skills",
        "skill_count", "is_noise_row",
    ]
    fieldnames = (
        [c for c in preferred if c in all_columns]
        + sorted(c for c in all_columns if c not in preferred)
    )

    with open(out_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(normalized_rows)

    print(f"Output rows: {len(normalized_rows)}")
    print(f"Written    : {out_path}")

    print_stats(normalized_rows)


if __name__ == "__main__":
    main()
