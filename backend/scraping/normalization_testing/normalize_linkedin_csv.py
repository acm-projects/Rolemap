# =============================================================================
# CONCEPT VOCABULARY LOADER
# =============================================================================
def load_concepts(script_dir: Path) -> tuple[list[str], list[str]]:
    """
    Load extractable and validate_only concept lists from concepts.json.
    Returns (extractable, all_concepts) where all_concepts = extractable + validate_only.
    """
    concepts_path = script_dir / "concepts.json"
    if not concepts_path.exists():
        print(
            "WARNING: concepts.json not found. Run 'python dump_concepts.py' first.\n"
            "         Falling back to synonym-only vocabulary (reduced coverage)."
        )
        return [], []

    with open(concepts_path, encoding="utf-8") as f:
        data = json.load(f)

    extractable = data.get("extractable", [])
    validate_only = data.get("validate_only", [])
    all_concepts = extractable + validate_only

    generated_from = data.get("generated_from", "unknown")
    counts = data.get("counts", {})
    print(
        f"Loaded concepts from {generated_from}: "
        f"{counts.get('extractable', len(extractable))} extractable, "
        f"{counts.get('validate_only', len(validate_only))} validate-only"
    )
    return extractable, all_concepts


# =============================================================================
# VOCABULARY BUILDER
# =============================================================================
def build_vocab(extractable_concepts: list[str]) -> set[str]:
    """
    Build the full extraction vocabulary:
      - all synonym keys and canonical values from SKILL_SYNONYMS
      - all extractable concept names (lowercased)
    Returns a set of lowercase strings for regex matching.
    """
    vocab = set()

    # Synonym keys and canonical values
    vocab.update(SKILL_SYNONYMS.keys())
    vocab.update(SKILL_SYNONYMS.values())

    # Extractable concepts (lowercased)
    for name in extractable_concepts:
        vocab.add(name.strip().lower())

    # Remove anything too short or blocked
    cleaned = set()
    for term in vocab:
        t = term.strip()
        if len(t) < 2:
            continue
        if t.upper() in BLOCKED_CONCEPTS:
            continue
        cleaned.add(t)

    return cleaned


# =============================================================================
# NORMALIZATION HELPERS
# =============================================================================
def normalize_skill(skill: str) -> str:
    """Apply synonym map, return canonical lowercase form."""
    if not skill:
        return ""
    s = skill.strip().lower()
    return SKILL_SYNONYMS.get(s, s)


def parse_skills(value) -> list[str]:
    """Parse pipe-separated or bracket-list skill strings."""
    if value is None:
        return []
    text = str(value).strip()
    if not text:
        return []
    if "|" in text:
        return [p.strip() for p in text.split("|") if p.strip()]
    if text.startswith("[") and text.endswith("]"):
        inner = text[1:-1].strip()
        if not inner:
            return []
        return [p.strip().strip('"').strip("'") for p in inner.split(",") if p.strip()]
    return [text]


def company_self_tokens(company: str) -> set[str]:
    """
    Build a set of lowercase tokens from the company name that should not
    appear as skills on that company's own job listings.

    Strategy: split on common separators, keep tokens >= 4 chars
    (avoids blocking single-letter or tiny words like "at", "the", "inc").
    Also include the full lowercased company name.

    Examples:
        "Notion"              → {"notion"}
        "Apple"               → {"apple"}
        "The New York Times"  → {"new york times", "york", "times"}
        "Nightfall AI"        → {"nightfall"}   (AI is in SHORT_SKILL_ALLOWLIST so keep it)
    """
    if not company:
        return set()
    tokens = set()
    name_lower = company.strip().lower()
    # Remove common legal suffixes that are never skills
    for suffix in (" inc", " llc", " ltd", " corp", " co", " group", " solutions",
                   " consulting", " technologies", " technology", " services",
                   " partners", " labs", " studio", " studios"):
        name_lower = name_lower.replace(suffix, "")
    name_lower = name_lower.strip()
    tokens.add(name_lower)
    # Individual words >= 4 chars
    for word in re.split(r"[\s\-_,&()./]+", name_lower):
        word = word.strip()
        if len(word) >= 4:
            tokens.add(word)
    return tokens


def is_noise_row(company: str, job: str, desc: str = "") -> bool:
    """
    Detect scraper-artifact rows that contain no real job data.
    Checks both the company/job fields and the description text.
    """
    blob = f"{company} {job}".upper()
    if any(p in blob for p in NOISE_PATTERNS):
        return True
    # Also catch rows where the description IS the LinkedIn login wall
    desc_upper = desc.upper()
    if any(p in desc_upper for p in NOISE_PATTERNS):
        return True
    return False


def is_job_title(skill_upper: str) -> bool:
    """Return True if this string is a role/job title, not a skill."""
    return skill_upper.lower() in JOB_TITLES


def is_valid_skill_label(skill: str) -> bool:
    """
    Gate on a candidate skill string before it enters the output.
    Returns False for:
      - empty or blank
      - blocked concepts
      - salary/compensation fragments
      - numeric fragments
      - too-long strings (likely sentence fragments)
      - pure punctuation / stray dashes
      - job title strings (roles, not skills)
    """
    if not skill:
        return False
    s = skill.strip()
    su = s.upper()

    if not s:
        return False
    if su in BLOCKED_CONCEPTS:
        return False
    if is_job_title(su):
        return False

    # Too long
    if len(s) > MAX_SKILL_LENGTH:
        return False

    # Compensation / numeric noise
    if re.search(r"\d", s):
        # Allow short acronyms with numbers like "3D", "Web3", "iOS"
        if len(s) > 6:
            return False
    if any(tok in su for tok in ["SALARY", "$", "CHARACTERS", "HOURS", "YEARS"]):
        return False

    # Short label filter
    if len(su) <= 2 and su not in SHORT_SKILL_ALLOWLIST:
        return False
    if len(su) == 3 and su not in SHORT_SKILL_ALLOWLIST and not su.isupper():
        return False

    # Stray punctuation
    if s.startswith("-") or s.endswith("-"):
        return False
    if s in {".", ",", ":", ";", "/", "|"}:
        return False

    return True


# =============================================================================
# FUZZY GRAPH VALIDATION
# =============================================================================
def build_concept_lookup(all_concepts: list[str]) -> dict[str, str]:
    """
    Build a dict mapping lowercase concept name -> original-case concept name
    for O(1) exact lookup and fuzzy fallback.
    """
    return {c.lower(): c for c in all_concepts}


def validate_against_graph(
    skill_lower: str,
    concept_lookup: dict[str, str],
    all_concepts_lower: list[str],
) -> str | None:
    """
    Try to match a skill string against the known concept vocabulary.
    Returns the canonical concept name if matched, else None.

    Strategy:
      0. Synonym-canonical shortcut — if the skill (or its synonym-normalized
         form) is a canonical value in SKILL_SYNONYMS, trust it directly.
         The synonym map is a curated allowlist; no graph hit needed.
      1. Exact lowercase match against graph concepts
      2. Synonym-normalize then exact match
      3. Fuzzy match (thefuzz, threshold FUZZY_MATCH_THRESHOLD)
    """
    # 0. Synonym-canonical shortcut
    if skill_lower in SYNONYM_CANONICALS:
        return skill_lower.upper()
    normed = normalize_skill(skill_lower)
    if normed in SYNONYM_CANONICALS:
        return normed.upper()

    # 1. Exact
    if skill_lower in concept_lookup:
        return concept_lookup[skill_lower]

    # 2. Synonym → exact
    if normed in concept_lookup:
        return concept_lookup[normed]

    # 3. Fuzzy fallback
    if _FUZZY_AVAILABLE and all_concepts_lower:
        best_score = 0
        best_concept = None
        for candidate_lower in all_concepts_lower:
            score = fuzz.ratio(skill_lower, candidate_lower)
            if score > best_score:
                best_score = score
                best_concept = candidate_lower
        if best_score >= FUZZY_MATCH_THRESHOLD and best_concept:
            return concept_lookup.get(best_concept, best_concept)

    return None


# =============================================================================
# CORE EXTRACTION
# =============================================================================
def extract_skills_from_text(text: str, vocab: set[str]) -> list[str]:
    """
    Scan free text for vocab terms using regex word-boundary matching.
    Returns a list of canonical (lowercased, synonym-normalized) skill strings.
    """
    if not text:
        return []

    content = text.lower()
    found = set()

    for term in vocab:
        # Use word-boundary regex; allow for punctuation breaks around the term
        pattern = r"(?<![a-z0-9\-\.])" + re.escape(term) + r"(?![a-z0-9\-\.])"
        if re.search(pattern, content):
            canonical = normalize_skill(term)
            if canonical:
                found.add(canonical)

    return sorted(found)


